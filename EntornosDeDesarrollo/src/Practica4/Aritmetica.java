package Practica4;

public class Aritmetica {
	public float suma(float primerSumando, float segundoSumando) {
		return primerSumando + segundoSumando;
	}

	public float resta(float primerSumando, float segundoSumando) {
		return primerSumando - segundoSumando;
	}

	public float multiplicacion(float primerSumando, float segundoSumando) {
		return primerSumando * segundoSumando;
	}

	public float division(float primerSumando, float segundoSumando) {
		return primerSumando / segundoSumando;
	}
}
